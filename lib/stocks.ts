// Deduplicate by ticker
const seen = new Set<string>();
export const STOCK_LIST: StockMeta[] = RAW
  .map(([ticker, name, sector]) => ({ ticker, name, sector }))
  .filter(s => {
    if (seen.has(s.ticker)) return false;
    seen.add(s.ticker);
    return true;
  });

export const TOTAL = STOCK_LIST.length;

export function getBatches(size = 12): StockMeta[][] {
  const out: StockMeta[][] = [];
  for (let i = 0; i < STOCK_LIST.length; i += size) {
    out.push(STOCK_LIST.slice(i, i + size));
  }
  return out;
}
